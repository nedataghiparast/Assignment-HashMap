public class Equations {

    public static double areaOfRectangle(double I, double w) {
        return I * w;
    }

    public static double areaOfTriangle(double h, double b) {
        return (h * b) / 2;
    }

    public static double circumferenceOfSphere(double r) {
        return Math.PI * 2 * r;
    }
}
